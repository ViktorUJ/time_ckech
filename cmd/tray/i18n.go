// Lang — поддерживаемые языки.
type Lang string

const (
	LangEN   Lang = "en"
	LangRU   Lang = "ru"
	LangIT   Lang = "it"
	LangES   Lang = "es"
	LangDE   Lang = "de"
	LangPL   Lang = "pl"
	LangZHTW Lang = "zh-TW"
)

// langOrder определяет порядок циклического переключения языков.
var langOrder = []Lang{LangRU, LangEN, LangIT, LangES, LangDE, LangPL, LangZHTW}

// langNames — отображаемые названия языков (на родном языке).
var langNames = map[Lang]string{
	LangEN:   "English",
	LangRU:   "Русский",
	LangIT:   "Italiano",
	LangES:   "Español",
	LangDE:   "Deutsch",
	LangPL:   "Polski",
	LangZHTW: "繁體中文",
}

var (
	currentLang Lang = LangRU // по умолчанию русский
	langMu      sync.RWMutex
)

// langFilePath возвращает путь к файлу с сохранённым языком.
func langFilePath() string {
	return filepath.Join(os.Getenv("ProgramData"), "ParentalControlService", "lang.txt")
}

// isValidLang проверяет, является ли язык поддерживаемым.
func isValidLang(l Lang) bool {
	for _, v := range langOrder {
		if v == l {
			return true
		}
	}
	return false
}

// loadLang загружает язык из файла при старте.
func loadLang() {
	data, err := os.ReadFile(langFilePath())
	if err != nil {
		return
	}
	l := Lang(strings.TrimSpace(string(data)))
	if isValidLang(l) {
		langMu.Lock()
		currentLang = l
		langMu.Unlock()
	}
}

func setLang(l Lang) {
	langMu.Lock()
	currentLang = l
	langMu.Unlock()
	// Сохраняем выбор на диск.
	_ = os.WriteFile(langFilePath(), []byte(string(l)), 0o644)
}

func getLang() Lang {
	langMu.RLock()
	defer langMu.RUnlock()
	return currentLang
}

// nextLang возвращает следующий язык в цикле.
func nextLang() Lang {
	cur := getLang()
	for i, l := range langOrder {
		if l == cur {
			return langOrder[(i+1)%len(langOrder)]
		}
	}
	return langOrder[0]
}

// tr возвращает перевод по ключу для текущего языка.
func tr(key string) string {
	lang := getLang()
	if m, ok := translations[lang]; ok {
		if v, ok := m[key]; ok {
			return v
		}
	}
	// Fallback to EN.
	if v, ok := translations[LangEN][key]; ok {
		return v
	}
	return key
}

